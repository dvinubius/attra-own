Moralis.Cloud.define("linkBalance", async (request) => {
  const balance = await LINK.methods
    .balanceOf(request.params.address)
    .call()
    .catch(() => "error");

  return balance;
});

Moralis.Cloud.define("ethUsdPrice", async (request) => {
  const latestRoundData = await EthUsd.methods
    .latestRoundData()
    .call()
    .catch(() => "error");

  return +latestRoundData.answer / 10 ** 8;
});
